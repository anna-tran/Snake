// subroutine to draw the walls
.globl	drawWall
drawWall:
	push {r4, r5, lr}

// changing y -> r1
	ldr	r1, =192
	mov	r4, r1		// counter for y
drawVertWall:
	mov	r0, #0
	mov	r2, #1
	mov	r3, #64
	bl	drawTile

	ldr	r0, =960
	mov	r1, r4		// reload r4 into r1
	mov	r2, #1
	mov	r3, #64
	bl	drawTile

	add	r1, r4, #64
	mov	r4, r1		// copy r1 into r4
	cmp	r4, #704
	ble	drawVertWall



	mov	r0, #64
	mov	r4, r0		// r4 is copy of r0
drawHorzWall:
	ldr	r1, =192	// y position
	mov	r2, #1		// draw wall
	mov	r3, #64		// size of image
	bl	drawTile

	mov	r0, r4
	ldr	r1, =704		// reload r4 into r1
	mov	r2, #1
	mov	r3, #64
	bl	drawTile

	add	r0, r4, #64
	mov	r4, r0		// copy r0 into r4
	ldr	r5, =959
	cmp	r4, r5
	ble	drawHorzWall

	pop {r4, r5, lr}
	mov	pc,lr

