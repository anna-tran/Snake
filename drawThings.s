// subroutine to put the random tiles
.globl putTiles
putTiles:

	push {r4, r5, r6, lr}

	// draw 5 vert tiles
	ldr	r4, =352	//to change the y
	mov	r5, #5
loop5:
	ldr	r0, =384
	mov	r1, r4		
	mov	r2, #0
	mov	r3, #32
	bl	drawTile
	add	r4, #32
	subs	r5, #1
	bne	loop5

	
	ldr	r4, =192	// to change the x
	mov	r5, #7
loop7:
