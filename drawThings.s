// subroutine to erase a tile
.globl eraseTile
// r0 is the x top left corner
// r1 is the y top left corner
// r2 color to replace
// r3 size of tile
eraseTile:
	push {r4-r9,lr}

// r6 is a copy of r0
// r7 is a copy of r1
	mov	r6, r0		
	mov	r7, r1

	// copy r2 into r5
	mov	r5, r2

// r8 = r0 + r3
// r9 = r1 + r3
	add	r8, r0, r3
	add	r9, r1, r3
	mov	r4, r7		// counter for y

eraseTileVert:
	mov	r3, r6		// counter for x
	mov	r0, r6		// reset x

eraseTileHorz:	
	mov		r2, r5		// get pixel color
	bl		DrawPixel
	add		r0, r3, #1	// x = x+1
	add		r3, r3, #1	// inc x counter
	cmp		r3, r8		// if <32 px across 
	blt		eraseTileHorz	// loop back

	add		r1, r4, #1	// y = y + 1
	add		r4, r4, #1	// inc y counter
	cmp		r4, r9		// if < 32px down
	blt		eraseTileVert	// loop back

	pop {r4-r9,lr}
	mov	pc,lr


.globl drawSnake
// r0 is length of Snake
// r1 is direction of snake
drawSnake:
	push {r4-r10,lr}
	mov	r9, r0		// r9 = length of snake
	mov	r8, r1		// r8 = direction of snake
	
	ldr	r4, =160	//x of tail
	ldr	r5, =288	//y of tail

	mov	r0, r4		// set x
	mov	r1, r5		// set y
	mov	r2, #4		// draw tail
	mov	r3, #32
	bl	drawTile

	ldr	r3, =snakePosition	// store the x and y coor of Snake tail in order at end of array
	lsl	r10, r9, #2
	add	r3, r9
	str	r0, [r3], #4		
	str	r1, [r3]

	sub	r9, #1		// discount tail
	add	r4, #32		// just add to x for now

//draw body
loopBody:
	
	mov	r0, r4		// get x
	mov	r1, r5		// get y
	mov	r2, #3		// draw body
	mov	r3, #32
	bl	drawTile
	
	ldr	r3, =snakePosition	//store x and y coor of snake body in order at ofset r9*4
	lsl	r10, r9, #2
	add	r3, r9
	str	r0, [r3], #4
	str	r1, [r3]
	
	add	r4, #32		// go to next x
	sub	r9, #1		// dec body length
	cmp	r9, #1
	bgt	loopBody

	mov	r0, r4
	mov	r1, r5

	cmp	r8, #9
	moveq	r2, #2
	cmp	r8, #10
	moveq	r2, #-2
	mov	r3, #32
	bl	drawTile

	ldr	r3, =snakePosition	//Store Head x and y coor at start of array
	lsl	r10, r9, #2
	add	r3, r9
	str	r0, [r3], #4
	str	r1, [r3]

	pop {r4-r10,lr}
	mov	pc,lr


.globl updateSnake
// r0 is length of snake
// r1 is directon of snake

updateSnake:
	push {r4-r9, lr}
	mov	r9, r0		//get length
	mov	r8, r1		//get direction
	ldr	r4, =snakePosition
	ldr	r5, =snakePosition
	ldr	r0, [r4], #4
	ldr	r1, [r4], #4
	mov	r6, r0		//copy x location
	mov	r7, r1		// copy y location


	cmp	r8, #8		//move right
	moveq	r2, #2
	addeq	r0, #32

	cmp	r8, #9		//move left
	moveq	r2, #-2
	subeq	r0, #32	

	cmp	r8, #10		//move down
	moveq	r2, #2
	addeq	r1, #32

	cmp	r8, #11		//move up
	moveq	r2, #2
	subeq	r1, #32	
	mov	r3, #32
	
	bl	drawTile	// draw head
	str	r0, [r5], #4
	str	r1, [r5], #4
	sub	r9, #1
reDrawBody:
	mov	r0, r6
	mov	r1, r7
	
	mov	r2, #3
	mov	r3, #32		//draw body piece
	bl	drawTile
	ldr	r6, [r4],#4
	ldr	r7, [r4],#4
	str	r0,[r5],#4
	str	r1,[r5],#4

	sub	r9, #1		// dec body length
	cmp	r9, #1



	bne	reDrawBody
	mov	r0, r6
	mov	r1,r7
	mov	r2, #4		// draw tail
	mov	r3, #32
	bl	drawTile
	
	ldr	r6, [r4],#4
	ldr	r7, [r4],#4
	str	r0,[r5],#4
	str	r1,[r5],#4

	mov	r0, r6
	mov	r1,r7
	ldr	r2, =0xA098		//erase previous tail
	mov	r3, #32
	bl	eraseTile
	

	pop {r4-r9, lr}
	mov	pc, lr

.globl	drawTile
// r0 is the x top left corner
// r1 is the y top left corner
// r2 int representing tile image to be displayed
// r3 size of tile
drawTile:
	push {r4, r5, r6, r7, r8, r9, r10, lr}
// 0 - Brick
// 1 - Wall
// 2 - Snake headR
// -2 - Snake headL
// 3 - Snake body
// 4 - Snake tail
// 5 - Apple
// 6 - Door
// 7 - Pointer

	cmp	r2, #0
	ldreq	r5, =Brick

	cmp	r2, #1
	ldreq	r5, =Hedgehog

	cmp	r2, #2
	ldreq	r5, =HeadR

	cmp	r2, #-2
	ldreq	r5, =HeadL

	cmp	r2, #3
	ldreq	r5, =Cat2

	cmp	r2, #4
	ldreq	r5, =Cat1

	cmp	r2, #7
	ldreq	r5, =Duck




// r6 is a copy of r0
// r7 is a copy of r1
	mov	r6, r0		
	mov	r7, r1

// r8 = r0 + r3
// r9 = r1 + r3
	add	r8, r0, r3
	add	r9, r1, r3
	mov	r4, r7		// counter for y

drawTileVert:
	mov	r3, r6		// counter for x
	mov	r0, r6		// reset x

drawTileHorz:	
	ldrh		r2, [r5], #2	// get pixel color


	bl		DrawPixel
	add		r0, r3, #1	// x = x+1
	add		r3, r3, #1	// inc x counter
	cmp		r3, r8		// if <32 px across 
	blt		drawTileHorz	// loop back

	add		r1, r4, #1	// y = y + 1
	add		r4, r4, #1	// inc y counter
	cmp		r4, r9		// if < 32px down
	blt		drawTileVert	// loop back


	mov		r0, r6		//return location of the tile
	mov		r1, r7
	pop {r4, r5, r6, r7, r8, r9, r10, lr}
	mov	pc, lr
		




/* Draw Pixel
 *  r0 - x
 *  r1 - y
 *  r2 - color
 */
.globl DrawPixel
DrawPixel:
	push	{r4}


	offset	.req	r4

	// offset = (y * 1024) + x = x + (y << 10)
	add		offset,	r0, r1, lsl #10
	// offset *= 2 (for 16 bits per pixel = 2 bytes per pixel)
	lsl		offset, #1

	// store the colour (half word) at framebuffer pointer + offset

	ldr	r0, =FrameBufferPointer
	ldr	r0, [r0]
	strh	r2, [r0, offset]

	pop		{r4}
	bx		lr


